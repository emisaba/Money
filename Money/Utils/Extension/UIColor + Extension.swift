import UIKit

extension UIColor {
    static func customYellow() -> UIColor {
        return UIColor(red: 250/255, green: 187/255, blue: 79/255, alpha: 1)
    }
    
    static func customNavyBlue() -> UIColor {
        return UIColor(red: 12/255, green: 20/255, blue: 57/255, alpha: 1)
    }
    
    static func customLightNavyBlue() -> UIColor {
        return UIColor(red: 42/255, green: 49/255, blue: 82/255, alpha: 1)
    }
    
    static func customWhite() -> UIColor {
        return UIColor(red: 235/255, green: 235/255, blue: 235/255, alpha: 1)
    }
}
